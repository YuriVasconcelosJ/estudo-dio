nomes = ["Ana", "Bruno", "Carlos", "Diana", "Eduardo", "Fernanda", "Gabriel", "Helena"]

# Os 3 primieros nomes da lista
tres_nomes = nomes[:3]
print(tres_nomes)

# Os 3 ultimos
tres_ultimos = nomes[-3:]
print(tres_ultimos)

# 2 ao 5


# Pares
nomes_pares = nomes[:: 2]
print(nomes_pares)

# Lista Invertida
nomes.reverse()
print(nomes)

